<h1>¿QUE ES EL CATOLICISMO?</h1>
<P>El catolicismo es la religión derivada del cristianismo que reconoce al papa de Roma como 
    la máxima autoridad espiritual. </P>

    <P>Esta se considera a sí misma como la única doctrina fundada por Jesucristo y encomendada por 
el apóstol Pedro, por lo que también se denomina a la iglesia católica como iglesia universal y 
se la distingue de la iglesia anglicana y el protestantismo.</P>

<P>A lo largo de la historia, el catolicismo se ha difundido por todo el mundo, 
principalmente por países occidentales y América, durante la época de colonización 
europea y hasta estos días. Actualmente se estima que existen más de 1.300 millones de fieles 
católicos bautizados.</P>
