<p>Su objetivo principal era unificar el Imperio romano mediante la religión, aunque el resultado no 
fue el esperado, ya que el catolicismo comenzó a expandirse rápidamente.</p>

<p>Durante la época del colonialismo europeo en el continente americano, la religión católica 
fue impuesta a los habitantes de dichos territorios. De esta manera el catolicismo logró 
extenderse por todo el mundo, conformando la religión con más fieles en el planeta.</p>
