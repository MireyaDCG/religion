                <h2>Las principales características y creencias del catolicismo son las siguientes:</h2>
<li>  
Se considera a sí misma como la única iglesia fundada por Cristo, encomendada para propagar la enseñanza cristiana a través del mundo.
            
</li>
                <li>Su doctrina fundamental se encuentra en el credo, donde se explican los fundamentos de la religión.</li>
                <li>Posee una organización jerárquica, conformada por el papa de Roma y los cardenales, y seguidos por el clero: los obispos, presbíteros y diáconos.</li>
                <li>La sede del catolicismo se encuentra en el Vaticano de Roma. Desde allí se administra la religión hacia todo el mundo.</li>
                <li>Su principal guía, junto con los concilios ecuménicos, es la Biblia.</li>
                <li> la misa constituye el rito principal de los católicos. Allí se realiza la eucaristía, la consagración del pan y el vino como cuerpo y sangre de Jesucristo.</li>
                <li>Acepta el culto a las imágenes y a los santos, a diferencia de otras religiones derivadas del cristianismo.</li>
                <li>María, la madre de Jesús, es considerada virgen y es venerada con especial devoción.</li>
                <li>Cree en la idea del purgatorio, sitio donde las almas se encuentran purificando sus pecados luego de la muerte.</li>
                <li>Ejerce 7 sacramentos que representan la gracia espiritual: el bautismo, la confirmación, la eucaristía, la penitencia, la unción de los enfermos, el orden sacerdotal y el matrimonio.</li>
